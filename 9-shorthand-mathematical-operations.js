let num = 10;

// Addition shorthand
num = num + 5;  // normal way
num += 5;       // shorthand (adds 5)
console.log(num); // 20

// Subtraction shorthand
num = num - 3;  // normal way
num -= 3;       // shorthand (subtracts 3)
console.log(num); // 17

// Multiplication shorthand
num = num * 2;  // normal way
num *= 2;       // shorthand (multiplies by 2)
console.log(num); // 34

// Division shorthand
num = num / 2;  // normal way
num /= 2;       // shorthand (divides by 2)
console.log(num); // 17

// Modulus shorthand
num = num % 5;  // normal way
num %= 5;       // shorthand (modulus 5)
console.log(num); // 2
